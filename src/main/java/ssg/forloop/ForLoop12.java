package ssg.forloop;

public class ForLoop12 {
   /*
    Aşağıdaki görüntüyü bir for döngüsü kullanarak elde eden kodu yazınız
    * * * * * *
    * * * * *
    * * * *
    * * *
    * *
    *
    */


    public static void main(String[] args) {

        int sayi=6;

        for(int i=1; i<=sayi; i++){
            for(int j=sayi; j>=i; j--){
                System.out.print("Gs ");
            }
            System.out.println();
        }





        }


    }

